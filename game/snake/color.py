import pygame

class Color:
    def __init__(self):
        self.red = pygame.Color(255,0,0)
        self.green = pygame.Color(0,255,0)
        self.black = pygame.Color(0,0,0)
        self.white = pygame.Color(255,255,255)
        self.dark_green = pygame.Color(0, 100, 0)
        self.grey = pygame.Color(128, 128, 128)